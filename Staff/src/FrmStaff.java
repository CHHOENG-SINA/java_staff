    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
       int index = table_data.getSelectedRow();
       if(index<0){
           JOptionPane.showMessageDialog(null, "Please Select Record to Remove!");
           return;
       }
       int click = JOptionPane.showConfirmDialog(null, "Do you want to Delete?","Delete",JOptionPane.YES_NO_OPTION);
       if(click==JOptionPane.NO_OPTION){return;}
       String staffid = table_data.getValueAt(index, 0).toString();
       try{
       String sqlremove = "Delete From tblstaff where staffid =" +staffid;
       Statement sm = DBManager.getConnection().createStatement();
       st.executeUpdate(sqlremove);
       loadDataToJTable("SELECT * FROM vstaff");
       }catch(Exception ex){
           JOptionPane.showMessageDialog(null, ex);
       }
    }//GEN-LAST:event_btndeleteActionPerformed

