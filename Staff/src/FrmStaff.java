     public void loadDataToJTable(String sql){
        try{
            rs = st.executeQuery(sql);
            String header[] = {"StaffId","Name","Department","Position","Salary"};
            int rowindex = 0;
            int row = 0;
            int col = 5;
            rs.last();
            row = rs.getRow();
            String data[][] = new String[row][col];
            rs.beforeFirst();
            double total_salary = 0 ;
//             Double.parseDouble(txtsalary.getText());
            while(rs.next()){
                data[rowindex][0] = rs.getString(1);
                data[rowindex][1] = rs.getString(2).trim();
                data[rowindex][2] = rs.getString(3).trim();
                data[rowindex][3] = rs.getString(4);
                data[rowindex][4] = rs.getString(5);
               total_salary += Double.parseDouble(rs.getString(5));
                rowindex +=1;
            }
            table_data.setModel(new DefaultTableModel(data,header));
            // Set Single Section 
            table_data.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            table_data.setColumnSelectionAllowed(false);
            rs.close();
           
           
            txtnum.setText(Integer.toString(table_data.getRowCount()));
            txtsalary.setText(Double.toString(total_salary));
        }catch (Exception ex){
            System.out.println(ex.toString());
        }
    }

